package Ch02;

public class C04자료형 {

	public static void main(String[] args) {
		// = (대입연산자)
		//공간=값
		// 대입연산자 오른쪽부터 먼저처리를 한다(우선순위)
		
		int num1;	//4byte 정수공간형성후 num1이름 부여
		num1 = 10;	//10값을 상수풀에 저장한 후 num1공간에 대입(복사)
		int num2 = 4;//4값을 상수풀에 저장한 후 4byte정수공간 num2에 초기화
		int num3 = num1 + num2; //num1의값+num2의값을 num3공간에 초기화
		System.out.println(num3);//num3의 값을 출력

		
		
		
		
	}

}
